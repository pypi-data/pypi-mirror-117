

def _private__function_add(x,y):
    """calculates the sum of x and y.     
    Parameters:     
       x (int): a number
       y (int): another number    
    Returns:     
       int: sum of x and y
    """  
    return x+y


def _private__function_sub(x,y):
    """calculates the subtraction of x and y.     
    Parameters:     
       x (int): a number
       y (int): another number    
    Returns:     
       int: Subtracted value of x and y
    """  
    return x-y


def _private__function_multiply(x,y):
    """calculates the multiplication of x and y.     
    Parameters:     
       x (int): a number
       y (int): another number    
    Returns:     
       int: multiplied x and y
    """  
    return x*y


def _private__function_divivde(x,y):
    """calculates the division of x and y.     
    Parameters:     
       x (int): a number
       y (int): another number    
    Returns:     
       int: Quotient of x and y
    """  
    return x/y